import 'package:flutter/material.dart';

/// method to add vertical height in UI components using SizedBox with
/// height parameter only
SizedBox sizedBoxHeight(double height) {
  return SizedBox(height: height);
}

/// method to add horizontal height in UI components using SizedBox with
/// width parameter only
SizedBox sizedBoxWidth(double width) {
  return SizedBox(width: width);
}